/*****************************************************************************
 * @name       :void LCD_direction(u8 direction)
 * @date       :2018-08-09
 * @function   :Setting the display direction of LCD screen
 * @parameters :direction:0-0 degree
                          1-90 degree
                                                                                                        2-180 degree
                                                                                                        3-270 degree
 * @retvalue   :None
******************************************************************************/
void LCD_direction(u8 direction) {
  lcddev.setxcmd = 0x2A;
  lcddev.setycmd = 0x2B;
  lcddev.wramcmd = 0x2C;
  switch (direction) {
  case 0:
    lcddev.width = LCD_W;
    lcddev.height = LCD_H;
    LCD_WriteReg(0x36,
                 (1 << 3) | (0 << 6) | (0 << 7)); // BGR==1,MY==0,MX==0,MV==0
    break;
  case 1:
    lcddev.width = LCD_H;
    lcddev.height = LCD_W;
    LCD_WriteReg(0x36, (1 << 3) | (0 << 7) | (1 << 6) |
                           (1 << 5)); // BGR==1,MY==1,MX==0,MV==1
    break;
  case 2:
    lcddev.width = LCD_W;
    lcddev.height = LCD_H;
    LCD_WriteReg(0x36,
                 (1 << 3) | (1 << 6) | (1 << 7)); // BGR==1,MY==0,MX==0,MV==0
    break;
  case 3:
    lcddev.width = LCD_H;
    lcddev.height = LCD_W;
    LCD_WriteReg(0x36,
                 (1 << 3) | (1 << 7) | (1 << 5)); // BGR==1,MY==1,MX==0,MV==1
    break;
  default:
    break;
  }
}
